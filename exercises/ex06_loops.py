# Task 2: Use a while loop to print numbers from 10 down to 1.

# Write your code below:


for i in range(1, 11):
    print(f"going up: {i}")


numbers = 10
while numbers >= 1:
    print(f"decreasing value: {numbers}")
    numbers -= 1
